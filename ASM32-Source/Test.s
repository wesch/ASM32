; ======================================
; Example program for binary file format
; ======================================



H:		.EQU	52
; ----------------------------------------------------------------------
M1:		.MODULE		
; ----------------------------------------------------------------------

GREG1:		.REG	R1

V1:		.WORD  	100
V2:		.HALF  	150
V3:		.WORD  	170


BEGIN:		LDW 	ARG0, V1
		LDW	ARG1, V3
		B	FUNC1, RL

; ----------------------------------------------------------------------
F1:		.FUNCTION
; ----------------------------------------------------------------------
FUNC1:	ST		RL,-8(SP)

		LDO		SP, 56(SP)
V4:		.WORD	250

		ADD	ARG0, ARG1, V4				
		BV (RL)
; ----------------------------------------------------------------------
		.ENDFUNCTION

		.ENDMODULE
; ----------------------------------------------------------------------			
	        .END 			
; ----------------------------------------------------------------------


	
